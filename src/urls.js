const strings = require('./strings');

const toUrl = (value, baseUrl) => {
  if (!strings.isValid(value)) { return undefined; }
  try {
    const url = new URL(value, baseUrl);
    return url;
  } catch (ex) {
    return undefined;
  }
};

const isUrl = value => {
  const url = toUrl(value);
  return (url && strings.isValid(url.protocol));
};

module.exports = {
  toUrl,
  isUrl
};

